
package com.mycompany.karsilastirmaoperatorleri;


public class KarsilastirmaOperatorleri {

    public static void main(String[] args) {
        /*
        ==   Eşit mi?
        !=   Eşit değil mi?
        >    Büyük mü?
        >=   BÜyük veya Eşit mi?
        <    Küçük mü?
        <=   KÜçük veya Eşit mi?
        */
        System.out.println(3 == 4);
        System.out.println(3 != 4);
        System.out.println(2 > 4);
        System.out.println(2 >= 1);
    }
}
